@implementation AllListsViewController
@synthesize dataModel;

- (id)initWithCoder:(NSCoder *)aDecoder
{
    if ((self = [super initWithCoder:aDecoder]))
    {
        self.dataModel = [[DataModel alloc] init];
    }
    return self;
}

